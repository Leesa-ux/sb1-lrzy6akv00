// Helper pour recherche fuzzy (optionnel)
export function searchCommune(query: string): string[] {
  const q = query.toLowerCase();
  return Object.entries(BELGIAN_COMMUNES)
    .filter(([code, name]) =>
      code.includes(q) ||
      name.toLowerCase().includes(q)
    )
    .map(([_, name]) => name)
    .slice(0, 10);
}
