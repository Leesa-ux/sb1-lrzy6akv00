export function isTempEmail(email: string): boolean {
  if (!email || typeof email !== "string") return false;

  const domain = email.split("@")[1]?.toLowerCase();
  if (!domain) return false;

  return TEMP_EMAIL_DOMAINS.includes(domain);
}
